import type { MessageContentPart } from '../services/llm.js';

/**
 * System prompt for extracting structured data from PDF invoices/waybills.
 * Used with a vision-capable model (e.g. Gemini) that receives the PDF as base64.
 */
export const PDF_EXTRACTION_SYSTEM_PROMPT = `Ты — специализированная система для извлечения данных из строительных накладных, счетов-фактур и товарных накладных (ТОРГ-12, УПД, счёт на оплату).

## Задача
Извлеки ВСЕ строки материалов/товаров из табличной части документа.

## Правила извлечения

1. **Наименование (raw_name)**: Копируй ТОЧНО как написано в документе, включая все сокращения, ГОСТы, размеры, марки. Не исправляй опечатки, не переформатируй.

2. **Единица измерения (unit)**: Нормализуй по следующим правилам:
   - м.кв, м2, кв.м → м²
   - м.куб, м3, куб.м → м³
   - шт., штук, штука → шт
   - м.п., пог.м, п.м., погонный метр → м.п.
   - кг., килограмм → кг
   - т., тонна → т
   - компл., комплект → компл
   - уп., упаковка → уп
   - л., литр → л
   - рул., рулон → рул

3. **Количество (quantity)**: Число. Если указано с запятой как десятичный разделитель — преобразуй в точку.

4. **Цена за единицу (unit_price)**: Число или null если отсутствует.

5. **Сумма (total_price)**: Число или null если отсутствует.

6. **Пропускай строки**:
   - Итого / Всего / В том числе НДС / Итого с НДС
   - Заголовки разделов
   - Пустые строки
   - Строки с услугами доставки (если явно не является материалом)

## Формат ответа

Ответь ТОЛЬКО валидным JSON без markdown-обёртки:

{
  "document_type": "строка — тип документа (накладная, счёт-фактура, УПД, счёт на оплату и т.д.)",
  "supplier_name": "строка или null — наименование поставщика",
  "document_number": "строка или null — номер документа",
  "document_date": "строка или null — дата документа",
  "items": [
    {
      "row_number": 1,
      "raw_name": "точное наименование из документа",
      "unit": "нормализованная единица",
      "quantity": 10.5,
      "unit_price": 150.00,
      "total_price": 1575.00
    }
  ],
  "totals": {
    "subtotal": null,
    "vat": null,
    "total": null
  }
}

## Важно
- Извлекай ВСЕ строки, даже если их много
- НЕ пропускай строки с длинными наименованиями
- НЕ объединяй похожие позиции
- Каждая строка таблицы = отдельный элемент в items
- row_number — это порядковый номер строки начиная с 1 (или номер позиции из документа, если он указан)`;

/**
 * Builds the user message content for PDF extraction.
 * PDF передаётся через content-part типа `file` (OpenRouter PDF inputs),
 * а не через `image_url`, который предназначен для картинок.
 */
export function buildPdfExtractionUserContent(base64Pdf: string): MessageContentPart[] {
  return [
    {
      type: 'text',
      text: 'Извлеки все позиции материалов из этого документа. Ответь строго в JSON формате.',
    },
    {
      type: 'file',
      file: {
        filename: 'invoice.pdf',
        file_data: `data:application/pdf;base64,${base64Pdf}`,
      },
    },
  ];
}
